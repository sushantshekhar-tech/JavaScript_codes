//comparsion operators
// ==   --> equals to
// !=   --> not equals
// ===  --> equal value and type
// !==  --> not equal value and type
// >    --> greater than
// <    --> less than
// >=   --> greater than or equal to
// <=   --> less than or equal to
// ?    --> Ternary operator

let a = 7
let b = "7"
console.log("A == B",a==b)
console.log("A != B",a!=b)
console.log("A === B",a===b)
console.log("A !== B",a!==b)