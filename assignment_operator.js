//coding wizard
//Assignment Operator
//  =          --> a = y
// +=          --> a = a + y
// -=          --> a = a - y
// *=          --> a = a * y
// /=          --> a = a / y
// %=          --> a = a % y
// **=         --> a = a ** y

let a = 10;
// a+=5 is same as a + 5



console.log("A + 5 =",a+=5);
console.log("A - 5 =",a-=5);
console.log("A * 5 =",a*=5);
console.log("A / 5 =",a/=5);
console.log("A % 5 =",a%=5);
console.log("A ** 5 =",a**=5);
